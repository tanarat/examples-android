package com.example.dotapp;

public class Dot {
	private int x;
	private int y;
	public Dot(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	public Dot() {
		super();
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
}
